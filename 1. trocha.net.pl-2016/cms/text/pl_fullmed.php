<h1>FullMed</h1>
	Aplikacja webowa, służąca do rejestracji wizyt pacjentów wraz z panelem administracyjnym i możliwością indywidualnego logowania się lekarzy o różnych specjalnościach. Projekt szkoleniowy wykonany w całości za pośrednictem Frameworka Laravel. Projekt dostępny na platformie GitHub.
