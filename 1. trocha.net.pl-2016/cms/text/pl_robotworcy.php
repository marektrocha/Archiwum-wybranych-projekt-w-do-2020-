<h1>Robotyka<BR>i programowanie</h1>
	Robotworcy.pl - autorski projekt, wspierający robotykę i programowanie wśród dzieci w wieku powyżej 5. roku życia. Zajęcia prowadzone w klubach dla dzieci i przedszkolach. Oprócz tworzenia konstrukcji z zestawów Lego WeDo 2.0 dzieci uczą się programowania m.in. za pośrednictwem oprogramowania Scretch. Na czas pandemii COVID-19 zajęcia zawieszone do odwołania.
